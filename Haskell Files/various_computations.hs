--- various_computations.hs contains some simple numeric function
--- definitions

----------------------------------------------------------------------
-- squareArea

squareArea x = x * x

----------------------------------------------------------------------
-- circleArea

circleArea r = pi * sqrt ( r )

----------------------------------------------------------------------
-- blueAreaCube

blueAreaCube l = 6 * circleArea( l / 4 ) 

----------------------------------------------------------------------
-- paintedCube1

paintedCube1 n = if ( n <= 2 ) then 0 else 6 * sqrt ( n - 2 )


----------------------------------------------------------------------
-- paintedCube2

paintedCube2 n = if ( n <= 2 ) then 0 else ( n - 2 ) * 12
